namespace Forge.Forms.Annotations
{
    /// <summary>
    /// Determines placement of an element.
    /// </summary>
    public enum Placement
    {
        /// <summary>
        /// Element is added in a row before the element.
        /// </summary>
        Before,

        /// <summary>
        /// Element is added in a row after the element.
        /// </summary>
        After,

        /// <summary>
        /// Element is added inline with the element.
        /// </summary>
        Inline
    }
}
